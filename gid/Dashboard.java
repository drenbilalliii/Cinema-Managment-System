    public void loadch(){
        try {
            String resultForChartOne [] = streamingFilmatRepository.repeatedFilmMax(); // max;
            String resultForChartOneFilmiMin []= streamingFilmatRepository.repeatedFilmMin(); //min
            
            
            Integer emriFilmitMax = Integer.parseInt(resultForChartOne[0]);
            Integer emriFilmitMin = Integer.parseInt(resultForChartOneFilmiMin[0]);
            
            Filmi f = filmiRepository.findByID(emriFilmitMax);
            Filmi fmin = filmiRepository.findByID(emriFilmitMin);
            
            Integer numriFilmitMax = Integer.parseInt(resultForChartOne[1]);
            Integer numriFilmitMin = Integer.parseInt(resultForChartOneFilmiMin[1]);
            
            DefaultPieDataset pieDataset = new DefaultPieDataset();
            
            
            pieDataset.setValue(f.getEmriFilmit(),numriFilmitMax);
            pieDataset.setValue(fmin.getEmriFilmit(), numriFilmitMin);
            
            JFreeChart chart = ChartFactory.createPieChart3D(null, pieDataset);
           
            PiePlot p= (PiePlot)chart.getPlot();
            
            // p.setForegroundAlpha(TOP_ALIGNMENT);
            ChartPanel chartPanel = new ChartPanel(chart);
            chart.getPlot().setBackgroundPaint(new Color(255,255,255));
           
            panelforChart1.removeAll();
            panelforChart1.add(chartPanel,BorderLayout.CENTER);
            chartPanel.setVisible(true);
            panelforChart1.validate();
        } catch (FilmiException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
  
       

