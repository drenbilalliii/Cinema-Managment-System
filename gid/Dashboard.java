    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
       if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
                
                String choice = jTextField1.getText();
                System.out.println("d");
                switch(choice){
                    case "Book a Ticket":
                        this.dispose();
                        AddTicketForm a = new AddTicketForm();
                        a.setVisible(true);
                        break;
                    case "Movie Slider":
                        this.dispose();
                        MovieSliderFrame m = new MovieSliderFrame();
                        m.setVisible(true);
                        break;
                    case "Show Movies":
                        this.dispose();
                        TableStreamingShow s = new TableStreamingShow();
                        s.setVisible(true);
                        break;
                    case "Add a Movie":
                        this.dispose();
                        AddAMovieForm aa = new AddAMovieForm();
                        aa.setVisible(true);
                        break;
                    case "Database":
                        this.dispose();
                        DashboardDatabase d = new DashboardDatabase();
                        d.setVisible(true);
                        break;
                    default:
                        JOptionPane.showMessageDialog(this,"Nuk gjendet ne listen tone");
                } 
                
            }
    }//GEN-LAST:event_jTextField1KeyReleased

    
