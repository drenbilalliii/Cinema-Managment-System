    public void updateClock(){
        Thread clock = new Thread(new Runnable() {

            @Override
            public void run() {
               try{
                   for(;;){
                       Calendar calendar = new GregorianCalendar();
                       
                       int sekondi = calendar.get(Calendar.SECOND);
                       int minuta = calendar.get(Calendar.MINUTE);
                       int hour = calendar.get(Calendar.HOUR);
                       
                       jLabel11.setText(hour + ":" + minuta +":" + sekondi);
                       
                       
                       Thread.sleep(1000);
                       
               }
            }catch(InterruptedException e){
                e.printStackTrace();
            }
        }
           
    });
        clock.start();
        
    

    }
